int		ft_atoi(char *str)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		++str;
	while (*str == '+' || *str == '-')
		if (*(str++) == '-')
			j = j * -1;
	while ('0' <= *str && *str <= '9')
	{
		i = i * 10;
		i = i + (j * (*(str++) - '0'));
	}
	return (i);
}
